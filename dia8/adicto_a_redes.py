cantidad_minutos = [120, 50, 600, 30, 90, 10, 200, 0, 500]
separador =[]
for tiempo in cantidad_minutos:
    if tiempo < 90:
        separador.append('bien')
    else:
        separador.append('mal') 
print(separador)
# comprehension
separador2=['bien'if tiempo<90 else 'mal' for tiempo in cantidad_minutos]        
print(separador2)